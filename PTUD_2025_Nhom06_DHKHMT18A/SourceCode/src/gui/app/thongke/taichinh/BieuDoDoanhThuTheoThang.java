    private void updateChart() {
        if (!java.beans.Beans.isDesignTime()) {
//            panelShadow1.remove(blankPlotChart1);
//            blankPlotChart1 = new com.raven.chart.Chart();
//            panelShadow1.add(blankPlotChart1);
//            panelShadow1.revalidate();
//            panelShadow1.repaint();
            
            ThongKeDAOImpl dao = new ThongKeDAOImpl();
            String loaiThongKe = cboChonLoai.getSelectedItem().toString();
            List<TongTienBanDuoc> results = null;
            java.text.SimpleDateFormat sdf = null;
            
            if (loaiThongKe.equals("Theo ngày")) {
                results = dao.getTongTienBanDuocTheoNgay();
                sdf = new java.text.SimpleDateFormat("dd/MM");
            } else if (loaiThongKe.equals("Theo tháng")) {
                results = dao.getTongTienBanDuocTheoThang();
                sdf = new java.text.SimpleDateFormat("MM/yyyy");
            } else if (loaiThongKe.equals("Theo năm")) {
                results = dao.getTongTienBanDuocTheoNam();
                sdf = new java.text.SimpleDateFormat("yyyy");
            }
            
            if (results != null && !results.isEmpty()) {
                ArrayList<String> dsLabel = new ArrayList<>();
                ArrayList<Double> dsSoTien = new ArrayList<>();
                
                for (TongTienBanDuoc tt : results) {
                    dsLabel.add(sdf.format(tt.getNgay()));
                    dsSoTien.add(tt.getTongTien() / 1000000.0);
                }
                
                double newMax = 0;
                for (Double val : dsSoTien) {
                    if (val > newMax) {
                        newMax = val;
                    }
                }
            // Reset max của chart để trục tự tính toán theo dữ liệu mới.
                blankPlotChart1.setChartMaxValues(newMax);
                blankPlotChart1.clear();
                for (int i = 0; i < dsSoTien.size(); i++) {
                    double value = dsSoTien.get(i);
                    blankPlotChart1.addData(new ModelChart(dsLabel.get(i), new double[]{ value, value }));
                }
                blankPlotChart1.start();
            }
        }
    }
