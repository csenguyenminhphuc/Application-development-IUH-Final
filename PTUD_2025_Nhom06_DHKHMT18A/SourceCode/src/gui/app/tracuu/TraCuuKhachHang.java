    private void loadAllKhachHang() {
        DefaultTableModel model = (DefaultTableModel) jTableKhachHang.getModel();
        model.setRowCount(0);

        ArrayList<KhachHang> results = khachHangDAO.layTatCaKhachHang();
        if (results == null || results.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không có khách hàng nào trong hệ thống.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        for (KhachHang kh : results) {
            model.addRow(new Object[]{kh.getMaKH(), kh.getTenKH(), kh.getSoDienThoai(), kh.getCccd()});
        }
    }
