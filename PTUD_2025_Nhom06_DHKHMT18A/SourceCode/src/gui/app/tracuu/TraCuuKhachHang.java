    private void traCuuKhachHang() {
        String tenKH = jTextFieldtenKhachHang.getText().trim();
        String soDienThoai = jTextFieldSDT.getText().trim();
        String cccd = jTextFieldCCCD.getText().trim();

        // Basic input validation
        if (tenKH.isEmpty() && soDienThoai.isEmpty() && cccd.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ít nhất một tiêu chí tìm kiếm!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validate phone number and CCCD format
        if (!soDienThoai.isEmpty() && !soDienThoai.matches("\\d{10,11}")) {
            JOptionPane.showMessageDialog(this, "Số điện thoại phải là 10 hoặc 11 chữ số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!cccd.isEmpty() && !cccd.matches("\\d{12}")) {
            JOptionPane.showMessageDialog(this, "CCCD phải là 12 chữ số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Use timKhachHang method from KhachHang_DAO
        ArrayList<KhachHang> listKH = khachHangDAO.timKhachHang(tenKH.isEmpty() ? null : tenKH, 
                                                               soDienThoai.isEmpty() ? null : soDienThoai, 
                                                               cccd.isEmpty() ? null : cccd);
        
        DefaultTableModel model = (DefaultTableModel) jTableKhachHang.getModel();
        model.setRowCount(0);
        
        if (listKH == null || listKH.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy khách hàng phù hợp.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        for (KhachHang kh : listKH) {
            model.addRow(new Object[]{kh.getMaKH(), kh.getTenKH(), kh.getSoDienThoai(), kh.getCccd()});
        }
    }

