    private void loadAllStations() {
        try {
            ArrayList<Ga> listGa = gaDAO.loadTatCaCaGa();
            DefaultTableModel model = (DefaultTableModel) jTableGa.getModel();
            model.setRowCount(0);

            if (listGa == null || listGa.isEmpty()) {
                System.out.println("No stations found in the database.");
                JOptionPane.showMessageDialog(this, "Danh sách ga trống.", "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            for (Ga ga : listGa) {
                Object[] row = new Object[]{
                    ga.getMaGa(),
                    ga.getTenGa(),
                    ga.getDiaChi(),
                    ga.getSoDienThoai()
                };
                model.addRow(row);
            }
            System.out.println("Loaded " + listGa.size() + " stations.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Không thể tải danh sách ga: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

