    private void traCuuGa() {
        String tenGa = jTextFieldtenGa.getText().trim();
        if (tenGa.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên ga!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            loadAllStations(); // Reload all stations if input is empty
            return;
        }

        try {
            ArrayList<Ga> filteredGa = gaDAO.timGaTheoTen(tenGa);
            DefaultTableModel model = (DefaultTableModel) jTableGa.getModel();
            model.setRowCount(0);

            if (filteredGa == null || filteredGa.isEmpty()) {
                System.out.println("No stations found matching: " + tenGa);
                JOptionPane.showMessageDialog(this, "Không tìm thấy ga có tên chứa: " + tenGa, "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            for (Ga ga : filteredGa) {
                Object[] row = new Object[]{
                    ga.getMaGa(),
                    ga.getTenGa(),
                    ga.getDiaChi(),
                    ga.getSoDienThoai()
                };
                model.addRow(row);
            }
            System.out.println("Found " + filteredGa.size() + " stations matching: " + tenGa);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tra cứu ga: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
    

